const GEMINI_MODEL = "gemini-2.5-flash";

export async function callGemini(systemPrompt, userMessage) {
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) throw new Error("GEMINI_API_KEY পরিবেশ চলকে সেট করা নেই");

  const url = `https://generativelanguage.googleapis.com/v1beta/models/${GEMINI_MODEL}:generateContent?key=${apiKey}`;

  const res = await fetch(url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      system_instruction: { parts: [{ text: systemPrompt }] },
      contents: [{ parts: [{ text: userMessage }] }],
      generationConfig: {
        temperature: 0.1,
        maxOutputTokens: 8192,
        responseMimeType: "application/json",
      },
    }),
  });

  if (!res.ok) {
    const err = await res.json().catch(() => ({}));
    const msg = err?.error?.message || `Gemini API Error: ${res.status}`;
    if (res.status === 429) throw new Error("রেট লিমিট — এক মিনিট পর আবার চেষ্টা করুন");
    if (res.status === 403 || res.status === 401) throw new Error("API Key ভুল — পুনরায় সেট করুন");
    throw new Error(msg);
  }

  const data = await res.json();
  const finishReason = data?.candidates?.[0]?.finishReason;
  if (finishReason === "MAX_TOKENS") throw new Error("উত্তর অসম্পূর্ণ — ছোট টেক্সট দিয়ে আবার চেষ্টা করুন");

  const raw = data?.candidates?.[0]?.content?.parts?.[0]?.text || "{}";
  const clean = raw.replace(/```json|```/g, "").trim();

  try {
    return JSON.parse(clean);
  } catch {
    // Attempt to recover truncated JSON
    let fixed = clean;
    if ((fixed.match(/"/g) || []).length % 2 !== 0) fixed += '"';
    const ob = (fixed.match(/{/g) || []).length, cb = (fixed.match(/}/g) || []).length;
    const oq = (fixed.match(/\[/g) || []).length, cq = (fixed.match(/\]/g) || []).length;
    for (let i = 0; i < oq - cq; i++) fixed += "]";
    for (let i = 0; i < ob - cb; i++) fixed += "}";
    try { return JSON.parse(fixed); }
    catch { throw new Error("AI থেকে সঠিক উত্তর পাওয়া যায়নি — আবার চেষ্টা করুন"); }
  }
}

